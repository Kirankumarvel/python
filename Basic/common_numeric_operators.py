# Common Numeric Operators

# Addition (+)
result = 5 + 3
print(result)  # 8

# Subtraction (-)
result = 10 - 4
print(result)  # 6

# Multiplication (*)
result = 7 * 6
print(result)  # 42

# Division (/)
result = 20 / 5
print(result)  # 4.0

# Exponentiation (**)
result = 2 ** 3
print(result)  # 8

# Modulo (%)
result = 10 % 3
print(result)  # 1

