# Example of if Statement
x = 5
if x < 10:
    print("x is less than 10")
if x > 20:
    print("x is greater than 20")

# Nested if Statements
x = 42
if x > 1:
    print("x is greater than 1")
    if x < 100:
        print("x is less than 100")

# if-else Statements
x = 4
if x > 2:
    print("x is greater than 2")
else:
    print("x is not greater than 2")

