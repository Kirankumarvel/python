# Operators and Expressions

x = 10
y = 5

# Addition
print(x + y)  # 15

# Subtraction
print(x - y)  # 5

# Multiplication
print(x * y)  # 50

# Division
print(x / y)  # 2.0

# Modulo
print(x % y)  # 0

# Order of Evaluation
print((x + y) * 2)  # 30
print(x ** y)  # 100000
print(x * y / 2)  # 25.0
print(x + y - 2)  # 13

# Using Parentheses
print((x + y) * (x - y))  # 75