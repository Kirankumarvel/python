# Initial Variable Assignments
xx = 2
jj = 23

# Addition
xX = xx + 2  # 4

# Modulo (Remainder)
kk = jj % 5  # 3

# Printing Values
print(xx)  # 2
print(kk)  # 3

# Multiplication
yy = 440 * 12  # 5280

# Exponentiation (Power)
print(4 ** 3)  # 64

# Printing the Result of Multiplication
print(yy)  # 5280

# Division
zz = yy / 1000  # 5.28

# Printing the Result of Division
print(zz)  # 5.28