# Initial assignment
x = 5
y = 2

# Using operators
sum_result = x + y          # Addition
diff_result = x - y         # Subtraction
prod_result = x * y         # Multiplication
div_result = x / y          # Division
mod_result = x % y          # Modulo

# Printing results
print("Sum:", sum_result)           # Sum: 7
print("Difference:", diff_result)   # Difference: 3
print("Product:", prod_result)      # Product: 10
print("Division:", div_result)      # Division: 2.5
print("Modulo:", mod_result)        # Modulo: 1

# Type conversion
a = "123"
b = int(a)               # String to integer
c = float(a)             # String to float

print("Integer:", b)     # Integer: 123
print("Float:", c)       # Float: 123.0