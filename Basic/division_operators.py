# Standard Division (/): Produces a floating-point result
print(10 / 2)  # 5.0
print(9 / 2)   # 4.5

# Floating-Point Division: When both operands are floats
print(10.0 / 2.0)    # 5.0
print(99.0 / 100.0)  # 0.99

# Floor Division (//): Produces an integer result by truncating the decimal part
print(10 // 2)  # 5
print(9 // 2)   # 4